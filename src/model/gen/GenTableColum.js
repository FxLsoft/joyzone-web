export default function (vm = {}) {
    let _ = this;
    _.id = vm.id || '';                     // 主键ID
    _.genTableId = vm.genTableId || '';     // 归属表编号
    _.name = vm.name || '';                 // 名称
    _.comments = vm.comments || '';         // 描述
    _.jdbcType = vm.jdbcType || '';         // 列的数据类型(字节长度)
    _.javaType = vm.javaType || '';         // JAVA类型
    _.javaField = vm.javaField || '';       // JAVA字段名
    _.isPk = !!vm.isPk;                     // 是否主键
    _.isNull = !!vm.isNull;                 // 是否可为空
    _.isInsert = !!vm.isInsert;             // 是否为插入字段
    _.isEdit = !!vm.isEdit;                 // 是否编辑字段
    _.isList = !!vm.isList;                 // 是否列表字段
    _.isForm = !!vm.isForm;                 // 是否表单显示
    _.isQuery = !!vm.isQuery;               // 是否查询字段
    _.queryType = vm.queryType || '';       // 查询方式（等于、不等于、大于、小于、范围、左LIKE、右LIKE、左右LIKE）
    _.showType = vm.showType || '';         // 字段生成方案（文本框、文本域、下拉框、复选框、单选框、字典选择、人员选择、部门选择、区域选择）
    _.dictType = vm.dictType || '';         // 字典类型
    _.settings = vm.settings || '';         // 其它设置（扩展字段JSON）
    _.sort = vm.sort || 0;                  // 排序（升序）
    _.tableName = vm.tableName || '';       // 管理的查询表名
    _.fieldLabels = vm.fieldLabels || '';   // 查询表格展示Label ---名称|省市区
    _.fieldKeys = vm.fieldKeys || '';       // 查询表格的keys 与 fieldLabels对应  ---name|area
    _.searchLabels = vm.searchLabels || '';  // 搜索展示的Label  ---名称|省市区
    _.searchKeys = vm.searchKeys || '';     // 搜索的keys 与 searchLables对应 ---name|area
    _.validateType = vm.validateType || ''; // 验证类型
    _.minLength = vm.minLength;             // 最小长度
    _.maxLength = vm.maxLength;             // 最长长度
    _.minValue = vm.minValue;               // 最小值
    _.maxValue = vm.maxValue;               // 最大值
    _.dataUrl = vm.dataUrl;                 // 数据接口
    _.createBy = vm.createBy || '';         // 创建者
    _.createDate = vm.createDate || '';     // 创建时间
    _.updateBy = vm.updateBy || '';         // 更新者
    _.updateDate = vm.updateDate || '';     // 更新时间
    _.remarks = vm.remarks || '';           // 备注说明
    _.delFlag = vm.delFlag || 0;            // 逻辑删除标记（0：显示；1：隐藏）
}
